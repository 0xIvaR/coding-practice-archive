package com.Arithmetic_Ops;

public class IncreDecreOps {
    public static void main(String[] args) {

        // Increment and Decrement Operators

        int x = 1;

        // Increment Ops
        x += 1;
        x++;

        // Decrement Operators
        x -= 1;
        x--;

        System.out.println(x);
    }
}
